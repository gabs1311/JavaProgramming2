package day17_WhileAndDoWhileLoops;

public class WhileLoopIntro {
    public static void main(String[] args) {

        if (true){
            System.out.println("Wooden Spoon");// 1 time
        }
        System.out.println("=================================");

       while (true){
            System.out.println("Wooden Spoon");// the while is a repeated if .it will go
           // infinite times, because of the true word.
        }

    }
}
